/* Operadores de comparação
> maior que
>= maior que ou igual a 
< menor que 
<= menor que ou igual a 
== igualdade (valor) ** Não é recomendavel usar 
=== igualdade estrita (pega o valor e o tipo)
!= diferente (valor) ** Não é recomendável usar
!== diferente estrito (pega o valor e o tipo)
*/
 
console.log(10 > 5); // maior que 
console.log(10 >= 5); // maior ou igual
console.log(10 < 5); // menor
console.log(10 <= 5); // menor ou igual
console.log(10 == '10'); // não se usa
console.log(10 === '10'); // igualdade estrita
console.log(10 != '10'); // não se usa
console.log(10 !== '10'); // diferente estrito